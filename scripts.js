function leiaMaisEc(){
    var pontos=document.getElementById("pontosEc")
    var maisTexto=document.getElementById("maisEc")
    var btnLeiaMais=document.getElementById("btnLeiaMaisEc")

    if(pontos.style.display == "none"){
        pontos.style.display="inline";
        maisTexto.style.display="none";
        btnLeiaMais.style.display="Leia Mais";       
    }else{
        pontos.style.display="none";
        maisTexto.style.display="inline";
        btnLeiaMais.style.display="Leia Menos";
    }
}
